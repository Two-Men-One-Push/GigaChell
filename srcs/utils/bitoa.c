#include <stddef.h>

char	*bitoa(char *buf, int n)
{
	const char	*base = "9876543210123456789";
	size_t		i;

	i = 12;
	buf[--i] = 0;
	while (n != 0 || i == 11)
	{
		buf[--i] = base[(n % 10) + 9];
		if (n < 0 && n > -10)
			buf[--i] = '-';
		n /= 10;
	}
	return (&buf[i]);
}
